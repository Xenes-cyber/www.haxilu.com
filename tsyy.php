</tr> <tr> <td class="odd"><div id="info"></div> <table border="0" align="center" cellpadding="3" cellspacing="1" class="acss"> <tr> <td class="vcss">　　蔡东藩(1877―1945)，名J，字椿寿，号东藩，萧山临浦镇人。自幼聪颖好学，儿时阅读《资治通鉴》等史书，时人称为“神童”。光绪十七年(1891)中秀才。宣统元年(1909)中省优贡生。1910年朝考以优入选，翌年春赴福建以知县候补。因不满官场恶习，月余托病回乡。辛亥革命后，应好友之邀，到上海会文堂新记书局任编辑，修撰《高等小学论说文范》、《中等新论说文范》、《清史概论》等书。从1916年至1926年的十年间，蔡东藩写成历朝通俗通义，有：《前汉通俗演义》、《后汉通俗演义》、《两晋通俗演义》、《南北史通俗演义》、《唐史通俗演义》、《五代史通俗演义》、《宋史通俗演义》、《元史通俗演义》、《明史通俗演义》、《清史通俗演义》、《民国通俗演义》(部分)。全书共11部、45册、1040回、600余万字，记述了从公元前221年到公元1920年间发生的重大历史事件和重要历史人物。该书在史料上遵循“以正史为经，务求确凿，以轶闻为纬，不尚虚诬，，的原则；在体裁上突出“义以载事，即以道情”的特点，并且自写正文，自写批注，自写评述。在撰编《民国通俗演义》中，对清廷之腐败，予以抨击，其间曾收到恐吓信及子弹，迫其修改，蔡不变初衷。<br /></td> 
