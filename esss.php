          <td colspan="6" class="vcss"> 二十四史是我国古代二十四部正史的总称。即：《史记》（汉・司马迁）、《汉书》（汉・班固）、《后汉书》（南朝宋・范晔）、《三国志》（晋・陈寿）、《晋书》（唐・房玄龄等）、《宋书》（南朝梁・沈约）、《南齐书》（南朝梁・萧子显）、《梁书》（唐・姚思廉）、《陈书》（唐・姚思廉）、《魏书》（北齐・魏收）、《北齐书》（唐・李百药）、《周书》（唐・令狐德钡龋、《隋书》（唐・魏征等）、《南史》（唐・李延寿）、《北史》（唐・李延寿）、《旧唐书》（后晋・刘d等）、《新唐书》（宋・欧阳修、宋祁）、《旧五代史》（宋・薛居正等）、《新五代史》（宋・欧阳修）、《宋史》（元・脱脱等）、《辽史》（元・脱脱等）、《金史》（元・脱脱等）、《元史》（明・宋濂等）、《明史》（清・张廷玉等）。“正史”之名，始见于《隋书・经籍志》：“世有著述，皆拟班、马，以为正史。”清代乾隆皇帝钦定“二十四史”，“正史”一称即专指“二十四史”。按《四库全书》的规定，正史类“凡未经宸断者，则悉不滥登。盖正史体尊，义与经配，非悬诸令典，莫敢私增”，即未经皇帝批准，不得列入正史。<br />
      　　 二十四史总共3249卷，约有4000万字。它记叙的时间，从第一部《史记》记叙传说中的黄帝起，到最后一部《明史》记叙到明崇祯17年（公元1644年）止，前后历时4000多年，用统一的本纪、列传的纪传体编写。二十四史的内容非常丰富，记载了历代经济、政治、文化艺术和科学技术等各方面的事迹。</td>
